    private void cargarClientes() {
        // Limpiar el combo antes de cargar
        usersCombo.removeAllItems();
        
        // Obtener los clientes
        Map<String, Cliente> clientes = controlClientes.getClientes();
        
        // DEBUG: Ver cu√°ntos clientes hay
        System.out.println("========= DEBUG =========");
        System.out.println("Total de clientes: " + clientes.size());
        
        if (clientes.isEmpty()) {
            System.out.println(" No hay clientes en el HashMap");
            usersCombo.addItem("No hay clientes registrados");
        } else {
            // Llenar el combo con los nombres
            for (Cliente cliente : clientes.values()) {
                System.out.println("Cliente encontrado: " + cliente.getNombre());
                usersCombo.addItem(cliente.getNombre());
            }
        }
        System.out.println("========================");
    }

