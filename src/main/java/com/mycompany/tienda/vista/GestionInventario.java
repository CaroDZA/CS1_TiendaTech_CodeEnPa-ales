    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tablaInventario.getSelectedRow();

        if (filaSeleccionada < 0) {
            JOptionPane.showMessageDialog(this,
                    "Seleccione un producto de la tabla",
                    "Advertencia",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        String codigo = (String) modeloTabla.getValueAt(filaSeleccionada, 0);

        int confirmar = JOptionPane.showConfirmDialog(this,
                "¿Está seguro de eliminar este producto?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION);

        if (confirmar == JOptionPane.YES_OPTION) {
            SistemaVentas.getInventario().eliminarProducto(codigo);
            cargarInventario();
            JOptionPane.showMessageDialog(this,
                    "Producto eliminado",
                    "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            String codigo = txtCodigo.getText().trim();

            if (codigo.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Por favor ingrese el código de barras en el campo correspondiente",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            String input = JOptionPane.showInputDialog(
                    this,
                    "Ingrese la cantidad a agregar al stock:",
                    "Actualizar Stock",
                    JOptionPane.QUESTION_MESSAGE
            );

            // Verificar si el usuario canceló o no escribió nada
            if (input == null || input.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Operación cancelada o cantidad no ingresada.");
                return;
            }

            int cantidad = Integer.parseInt(input);

            SistemaVentas.getInventario().actualizarStock(codigo, cantidad);
            JOptionPane.showMessageDialog(this, "Stock actualizado correctamente");

            cargarInventario();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa una cantidad válida");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

