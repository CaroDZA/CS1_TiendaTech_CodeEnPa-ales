    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            String codigo = txtCodigo.getText().trim();
            String nombre = txtNombre.getText().trim();
            String precioStr = txtPrecio.getText().trim();
            String categoria = (String) comboCategoria.getSelectedItem();
            String stockStr = txtStock.getText().trim();
            String ubicacion = txtUbicacion.getText().trim();
            String descuentoStr = txtDescuento.getText().trim();

            if (codigo.isEmpty() || nombre.isEmpty() || precioStr.isEmpty()
                    || stockStr.isEmpty() || ubicacion.isEmpty() || descuentoStr.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Todos los campos son obligatorios",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            double precio = Double.parseDouble(precioStr);
            int stock = Integer.parseInt(stockStr);
            double descuento = Double.parseDouble(descuentoStr) / 100.0;

            ProductoFisico nuevo = new ProductoFisico(
                    nombre, precio, categoria, codigo, stock, ubicacion, descuento
            );

            SistemaVentas.getInventario().agregarProducto(nuevo);

            JOptionPane.showMessageDialog(this,
                    "Producto agregado exitosamente",
                    "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);

            limpiarCampos();
            cargarInventario();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Error en formato de números",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

